#include "libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	char	*final_dest;
	char	*final_src;

	if (!src && !dest)
		return (NULL);
	final_dest = dest;
	final_src = src;
	if (src < dest)
	{
		while (n--)
			((char *)dest)[n] = ((char *)src)[n];
	}
	else
		ft_memcpy(dest, src, n);
	return (dest);
}
