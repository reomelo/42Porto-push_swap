#include "libft.h"

long int	ft_atoi(const char *str)
{
	long int	res;
	int			signal;
	int			i;

	res = 0;
	signal = 1;
	i = 0;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signal = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = ((res * 10) + (str[i] - '0'));
		i++;
	}
	return (res * signal);
}
